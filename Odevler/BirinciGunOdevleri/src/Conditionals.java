
public class Conditionals {

	public static void main(String[] args) {
		int sayi = 24;
		if (sayi < 20) {
			System.out.println("Sayý 20'den küçüktür.");
		}

		else if (sayi == 20) {
			System.out.println("Sayý 10'dan küçüktür.");
		} else {
			System.out.println("Sayý 20'den büyüktür.");
		}

	}

}
